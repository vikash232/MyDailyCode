import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        Scanner scn = new Scanner(System.in);

        // write your code here
        int n = scn.nextInt();
        for (int i = n; i >= 1; i++) {
            for (int j = 1; j <= 1; j++) {
                System.out.println("*\t");
            }
            System.out.println();
        }
    }
}
