/*
_____$$$$$      Note: kth-->Space 
                      jth-->$'s
 */

public class Pattern9 {
    public static void main(String[] args) {
        for (int k = 1; k <= 5; k++) // no. of spaces
        {
            System.out.print(" ");
        }
        for (int j = 1; j <= 5; j++)// no. of $'s
        {
            System.out.print("$");
        }
        System.out.println();
    }
}
